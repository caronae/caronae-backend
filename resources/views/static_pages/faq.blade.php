  <div class="text">
    <ul class="w-list-unstyled perguntas">
      <li class="pergunta">
        <h3 class="pergunta-title">O que é o Caronaê UFRJ?</h3>
        <p class="resposta hidden">O Caronaê UFRJ é um sistema de caronas solidárias composto por dez pontos de caronas espalhados pelo campus Cidade Universitária UFRJ e por um aplicativo mobile exclusivo para a comunidade acadêmica da UFRJ (alunos, professores e servidores).</p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">Qual a relação do Caronaê com a UFRJ?</h3>
        <p class="resposta hidden">É um projeto idealizado por alunos da UFRJ para toda a comunidade acadêmica, tendo sido premiado no concurso Soluções Sustentáveis 2014, organizado pelo Fundo Verde da UFRJ. O sistema é hospedado nos servidores da UFRJ, e utiliza a base dados de registro para garantir a segurança do sistema.</p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">Quem pode utilizar o Caronaê UFRJ?</h3>
        <p class="resposta hidden">Qualquer pessoa que possua um registro ativo na Intranet UFRJ, seja professor, aluno ou servidor.</p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">Por que o sistema é seguro?</h3>
        <p class="resposta hidden">O login no sistema é feito através da Intranet UFRJ, garantindo que todas as pessoas que entram no aplicativo tem um vínculo ativo com a UFRJ. Para fazer login no aplicativo, siga os seguintes passos:
            <br>1. Faça login na Intranet UFRJ
            <br>2. No link “Caronaê” e gere sua chave de acesso
            <br>3. Insira seu CPF e sua chave de acesso no aplicativo, depois é só buscar e oferecer caronas =)
        </p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">O app é pago?</h3>
        <p class="resposta hidden">Tanto o download quanto o  uso do app serão totalmente gratuitos, não recaindo qualquer tipo de cobrança sobre os usuários.
        </p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">E as caronas em si serão pagas?</h3>
        <p class="resposta hidden">O Caronaê UFRJ é um sistema baseado nos princípios da colaboração, que se limita a conectar pessoas com destinos comuns. Por esse motivo o sistema é gratuito, sem associar movimentações  financeiras às caronas. Qualquer acordo feito entre motoristas e caronistas fica a cargo exclusivamente dos mesmos.
        </p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">O que são os pontos de carona?</h3>
        <p class="resposta hidden">São pontos estrategicamente posicionados entre os edifícios e os estacionamentos. Estão localizados em locais de grande movimento e visibilidade, servindo para facilitar o encontro entre os envolvidos na carona. Além disto, caronas espontâneas podem acontecercasoo caronista aguarde no ponto e consiga uma carona para seu destino com um motorista que esteja indo embora no momento.
        </p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">Tive meu celular roubado/perdido. O que devo fazer?</h3>
        <p class="resposta hidden">Para que não seja possível usar o aplicativo no celular roubado/perdido é necessário gerar uma nova chave via aplicativo ou pela sua conta da INTRANET, através do https://caronae.tic.ufrj.br/chave/. 
        </p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">Tive um problema. O que faço?</h3>
        <p class="resposta hidden">Entre em contato! O Caronaê possui diversos canais de comunicação. Você pode reportar um usuário diretamente pelo aplicativo, entrar em contato pelo canal Falaê no app ou da página na web (caronae.ufrj.br), enviar um email (caronae@fundoverde.ufrj.br) ou mandar uma mensagem na página do Facebook (https://www.facebook.com/caronaeufrj). 
        </p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">Há alguma previsão de expansão para outros campi da UFRJ (Praia Vermelha, Largo de São Francisco, Macaé, Xerém, etc.)?</h3>
        <p class="resposta hidden">Ainda não existe previsão para expansão do projeto para outros campi da UFRJ, no entanto, este será o primeiro passo para o crescimento do Caronaê UFRJ.</p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">Depois da formatura, posso continuar usando o Caronaê?</h3>
        <p class="resposta hidden">O Caronaê é exclusivo para os membros ativos da comunidade acadêmica da UFRJ. Ao se formar, sua matrícula não será considerada ativa e, consequentemente, você não terá mais acesso ao aplicativo. </p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">Como surgiu a ideia do Caronaê?</h3>
        <p class="resposta hidden">O sistema foi idealizado por estudantes da Universidade de diversos cursos que se sentiam insatisfeitos com a precariedade da mobilidade na Cidade Universitária. Após estudar os sistemas antecessores ao Caronaê, chegaram a uma nova solução para tentar facilitar a vida de toda a comunidade acadêmica.</p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">Qual o público do Caronaê?</h3>
        <p class="resposta hidden">Cerca de 7 mil alunos participam de algum tipo de sistema que auxilia o estudante a conseguir carona na Cidade Universitária, seja usando as redes sociais ou grupos no WhatsApp. O Caronaê entrevistou 701 estudantes, identificando que 53% pegam ou oferecem carona e 81% confiariam em um aplicativo destinado a caronistas.</p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">Quais os impactos diretos e indiretos do Caronaê?</h3>
        <p class="resposta hidden">Diretamente, o sistema irá contribuir para racionalizar o uso do transporte individual na Cidade Universitária ao aumentar a taxa de ocupação dos veículos, o que reduzirá congestionamentos, a demanda por vagas de estacionamento e a emissão de poluentes.
        Além disso, o Caronaê pretende aumentar a socialização, integrando alunos de cursos diferentes, ajudando a vencer a fragmentação da universidade e aumentar o sentido de pertencimento do aluno para com a instituição.</p>
      </li>
      <li class="pergunta">
        <h3 class="pergunta-title">Qual a base do projeto Caronaê?</h3>
        <p class="resposta hidden">O Caronaê foi elaborado com base em três elementos: o aplicativo; pontos de carona e mudança cultural por meio de uma campanha de marketing. Acreditamos que esses elementos, isoladamente, não trariam o resultado desejado. A ação deve ser conjunta e complementar entre três elementos.
        </p>
      </li>
    </ul>
